<template>
  <input
    :type="type"
    :class="['input', { 'input--error': error }]"
    :placeholder="placeholder"
    :value="modelValue"
    @input="$emit('update:modelValue', ($event.target as HTMLInputElement).value)"
  />
</template>

<script setup lang="ts">
withDefaults(defineProps<{
  type?: string
  placeholder?: string
  modelValue?: string
  error?: boolean
}>(), {
  type: 'text',
  placeholder: '',
  modelValue: '',
  error: false
})

defineEmits(['update:modelValue'])
</script>

